package class03;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a=10, b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(13%10); //remainder 3
        System.out.println(24%5);//4

        System.out.println(15/10);//1
        System.out.println(24/5);//4

    }
}
